package oops;

public class Calculator {
	public int add(int a, int b) {
		int c = a + b;
		return c;
	}
	
	public int add(int a,int b, int c) {
		return a+b+c;
	}
	
	public int substract(int a, int b) {
		int c = a - b;
		return c;
	}
	public int multiple(int a, int b) {
		int c = a * b;
		return c;
		
	}
	public float divide(float a, float b) {
		float c = a / b;
		return c;
	}
}
